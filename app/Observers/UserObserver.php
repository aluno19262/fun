    /**
     * Handle the User "updated" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function updated(User $user)
    {
        if(!empty($user->associate) && ($user->email != $user->associate->email || $user->name != $user->associate->name)){
            $user->associate->name = $user->name;
            $user->associate->email = $user->email;
            $user->associate->saveQuietly();
        }
    }

